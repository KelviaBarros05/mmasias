	static void imprimeMapa(String[] castillo, int[] posicion, int viewPort){

		for (int i=posicion[FILA]-viewPort; i<posicion[FILA]+viewPort; i++){
			for (int j=posicion[COLUMNA]-viewPort; j<posicion[COLUMNA]+viewPort; j++) {
				System.out.print(mapea(castillo[i].charAt(j)));
			}
			System.out.println();
		}	
	}

	static String mapea(char elemento){

        HashMap<String, String> tiles = new HashMap<>();
		
        tiles.put(" ", "   ");
		tiles.put(".", " . ");
		tiles.put("-", "[#]");
		tiles.put("=", "|||");
		tiles.put("|", "[#]");
		tiles.put(":", "oOo");
		tiles.put("+", "...");
		tiles.put("O", "[ ]");
		tiles.put("#", ".:.");
		tiles.put("*", "***");
		tiles.put("$", "$$$");
		tiles.put("X", "XXX");
		tiles.put("%", "%%%");

		return tiles.get("" + elemento);

	}

